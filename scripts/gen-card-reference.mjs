// Generates SPLENDOR_CARDS.md - a human-readable table of every development
// card and noble, for comparing the digital game against a physical/official
// Splendor set. Run with: npm run gen:cards
//
// Source of truth is splendor/packages/engine/data/splendor_data.json, the
// canonical data the shipped gameData.ts is generated from (a drift test,
// server/test/splendorEngine.test.ts "no drift", keeps the two identical, so
// this reflects exactly what the game plays with). The table is a literal
// rendering of that data - no transcription, so it can't disagree with the
// game by mistake. It is regenerated, never hand-edited.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const DATA = join(root, "splendor", "packages", "engine", "data", "splendor_data.json");
const OUT = join(root, "SPLENDOR_CARDS.md");

const COLORS = ["white", "blue", "green", "red", "black"];
const GEM = { white: "diamond", blue: "sapphire", green: "emerald", red: "ruby", black: "onyx" };
const TIERS = [1, 2, 3];

const { cards, nobles, meta } = JSON.parse(readFileSync(DATA, "utf8"));

// ---- sanity checks: a wrong-shaped data file must NOT yield a quiet table ----
let failed = false;
const check = (cond, msg) => {
  if (!cond) {
    console.error(`✗ ${msg}`);
    failed = true;
  }
};

check(cards.length === 90, `expected 90 cards, got ${cards.length}`);
check(nobles.length === 10, `expected 10 nobles, got ${nobles.length}`);

const perTier = { 1: 0, 2: 0, 3: 0 };
const perTierColor = { 1: {}, 2: {}, 3: {}, };
for (const c of cards) {
  perTier[c.tier] = (perTier[c.tier] ?? 0) + 1;
  perTierColor[c.tier][c.bonus] = (perTierColor[c.tier][c.bonus] ?? 0) + 1;
}
check(perTier[1] === 40 && perTier[2] === 30 && perTier[3] === 20, `tier split ${JSON.stringify(perTier)} != 40/30/20`);
const expectPerColor = { 1: 8, 2: 6, 3: 4 };
for (const t of TIERS) {
  for (const col of COLORS) {
    check(
      perTierColor[t][col] === expectPerColor[t],
      `tier ${t} has ${perTierColor[t][col] ?? 0} ${col} cards, expected ${expectPerColor[t]}`
    );
  }
}
const totalPrestige = cards.reduce((s, c) => s + c.points, 0);
check(totalPrestige === 140, `total prestige ${totalPrestige} != 140`);
check(
  nobles.every((n) => n.points === 3),
  "every noble should be worth 3 points"
);

if (failed) {
  console.error("Aborting: data failed its sanity checks; SPLENDOR_CARDS.md not written.");
  process.exit(1);
}

// ---- markdown helpers --------------------------------------------------------
const cell = (n) => (n ? String(n) : ""); // blank for zero, so real values pop
const costRow = (c) =>
  `| ${c.id} | ${cell(c.points)} | ${COLORS.map((col) => cell(c.cost[col])).join(" | ")} |`;

const pointDist = (t) => {
  const dist = {};
  for (const c of cards) if (c.tier === t) dist[c.points] = (dist[c.points] ?? 0) + 1;
  return Object.keys(dist)
    .sort((a, b) => a - b)
    .map((p) => `${dist[p]}×${p}pt`)
    .join(", ");
};

const out = [];
const P = (...lines) => out.push(...lines);

P(
  "<!-- GENERATED by scripts/gen-card-reference.mjs - do not edit by hand. Run: npm run gen:cards -->",
  "",
  "# Splendor card reference",
  "",
  "Every development card and noble in this game, for comparing against a",
  "physical or official Splendor set. Rendered directly from the game's",
  "canonical data, so it matches exactly what the game plays with.",
  "",
  "**In every cost table, a blank cell means 0** - only the gems you actually",
  "pay are shown. `Pts` is the prestige (victory) points the card is worth;",
  "a blank means 0 points. Cards are grouped by tier, then by the gem they",
  "produce (their **bonus**), which is how you'd sort a physical deck.",
  ""
);

// ---- summary -----------------------------------------------------------------
P(
  "## At a glance",
  "",
  "| | Tier 1 | Tier 2 | Tier 3 | Total |",
  "| --- | ---: | ---: | ---: | ---: |",
  `| Cards | ${perTier[1]} | ${perTier[2]} | ${perTier[3]} | ${cards.length} |`,
  `| Cards per gem | ${expectPerColor[1]} each | ${expectPerColor[2]} each | ${expectPerColor[3]} each | |`,
  `| Point spread | ${pointDist(1)} | ${pointDist(2)} | ${pointDist(3)} | |`,
  "",
  `- **Total prestige across all cards:** ${totalPrestige}`,
  `- **Nobles:** ${nobles.length}, each worth 3 points (each gem appears in exactly 5 noble requirements)`,
  `- **Goal:** ${meta?.targetPrestige ?? 15} prestige points to trigger the end of the game`,
  ""
);

const setup = meta?.setup;
if (setup) {
  const t = setup.tokensByPlayerCount ?? {};
  const nob = setup.noblesRevealedByPlayerCount ?? {};
  P(
    "**Setup by player count**",
    "",
    "| Players | Gem tokens (each color) | Gold (wild) | Nobles revealed |",
    "| --- | ---: | ---: | ---: |",
    ...["2", "3", "4"].map(
      (n) => `| ${n} | ${t[n]?.gemEach ?? "?"} | ${t[n]?.gold ?? "?"} | ${nob[n] ?? "?"} |`
    ),
    ""
  );
}

P(
  "**Gem colors → gem names** (the physical game uses gem names):",
  "",
  COLORS.map((c) => `${c} = ${GEM[c]}`).join(" · ") + " · gold = wild/joker",
  ""
);

// ---- cards by tier, then bonus color ----------------------------------------
for (const t of TIERS) {
  P(`## Tier ${t} cards (${perTier[t]})`, "");
  for (const col of COLORS) {
    const group = cards
      .filter((c) => c.tier === t && c.bonus === col)
      .sort((a, b) => a.id - b.id);
    P(
      `### ${col[0].toUpperCase() + col.slice(1)} bonus (${GEM[col]}) — ${group.length} cards`,
      "",
      "| ID | Pts | White | Blue | Green | Red | Black |",
      "| ---: | ---: | ---: | ---: | ---: | ---: | ---: |",
      ...group.map(costRow),
      ""
    );
  }
}

// ---- nobles ------------------------------------------------------------------
P(
  "## Nobles (10)",
  "",
  "Each noble is worth **3 points** and visits the first player who owns the",
  "listed number of cards of each gem (no tokens involved). Blank means 0.",
  "",
  "| # | Name | White | Blue | Green | Red | Black |",
  "| ---: | --- | ---: | ---: | ---: | ---: | ---: |",
  ...nobles
    .slice()
    .sort((a, b) => a.id - b.id)
    .map(
      (n) =>
        `| ${n.id} | ${n.name ?? ""} | ${COLORS.map((col) => cell(n.requirement[col])).join(" | ")} |`
    ),
  ""
);

writeFileSync(OUT, out.join("\n"));
console.log(`✓ ${cards.length} cards, ${nobles.length} nobles → ${OUT.replace(root + "/", "")}`);
